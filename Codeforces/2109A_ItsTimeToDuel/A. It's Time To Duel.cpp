void solve() {
    int n;
    cin >> n;

    vector<int> arr(n);

    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }

    bool flag = true;
    bool flag2 = false;
    for (int i = 0; i < n - 1; i++) {
        if (arr[i] == 0) flag2 = true;
        if (arr[i] == 0 && arr[i + 1] == 0) {
            flag = false;
            break;
        }
    }
    if (arr[n - 1] == 0) flag2 = true;

    if (flag && flag2) {
        cout << "NO" << endl;
    } else {
        cout << "YES" << endl;
    }
}

