void solve() {
    int n;
    cin >> n;

    string arr;
    cin >> arr;

    bool s = false, p = false;
    for (int i = 0; i < n; i++) {
        if (arr[i] == 's' && i != 0) s = true;
        if (arr[i] == 'p' && i != n - 1) p = true;
    }
    if (!s || !p) {
        cout << "YES" << endl;
        return;
    }
    else {
        cout << "NO" << endl;
        return;
    }
}

