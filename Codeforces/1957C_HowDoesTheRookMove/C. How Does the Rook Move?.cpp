template <int MOD = 1000000007>
struct Modular {
int value;
    static const int MOD_value = MOD;

    Modular(long long v = 0) { value = v % MOD; if (value < 0) value += MOD;}
    Modular(long long a, long long b) : value(0){ *this += a; *this /= b;}

    Modular& operator+=(Modular const& b) {value += b.value; if (value >= MOD) value -= MOD; return *this;}
    Modular& operator-=(Modular const& b) {value -= b.value; if (value < 0) value += MOD;return *this;}
    Modular& operator*=(Modular const& b) {value = (long long)value * b.value % MOD;return *this;}

    friend Modular mexp(Modular a, long long e) {
    Modular res = 1; while (e) { if (e&1) res *= a; a *= a; e >>= 1; }
    return res;
    }
    friend Modular inverse(Modular a) { return mexp(a, MOD - 2); }

    Modular& operator/=(Modular const& b) { return *this *= inverse(b); }
    friend Modular operator+(Modular a, Modular const b) { return a += b; }
    friend Modular operator-(Modular a, Modular const b) { return a -= b; }
    friend Modular operator-(Modular const a) { return 0 - a; }
    friend Modular operator*(Modular a, Modular const b) { return a *= b; }
    friend Modular operator/(Modular a, Modular const b) { return a /= b; }
    friend std::ostream& operator<<(std::ostream& os, Modular const& a) {return os << a.value;}
    friend bool operator==(Modular const& a, Modular const& b) {return a.value == b.value;}
    friend bool operator!=(Modular const& a, Modular const& b) {return a.value != b.value;}
};

const int maxn = 3 * 1e5;

vector<Modular<>> fac(maxn + 5);
vector<Modular<>> req(maxn + 5);

void precompute() {
    fac[0] = 1;
    for (int i = 1; i <= maxn; i++) 
        fac[i] = fac[i - 1] * i;
}

Modular<> combination(int n, int r) {
    return fac[n] / (fac[n - r] * fac[r]);
}

Modular<> func(int n) {
    return fac[n] / fac[n / 2];
}

void solve() {
    int n, m;
    cin >> n >> m;

    int rem = n;
    for (int i = 0; i < m; i++) {
        int a, b;
        cin >> a >> b;
        rem -= (a == b ? 1 : 2);
    }

    Modular<> ans = 0;
    for (int i = (rem & 1 ? 1 : 0); i <= rem; i += 2) {
        Modular<> v1 = combination(rem, i);
        Modular<> v2 = func(rem - i);
        ans += combination(rem, i) * func(rem - i);
    }

    cout << ans << endl;
}

